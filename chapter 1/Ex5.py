name = "Nguyễn Thủy Hỏa"
age = 18
average_score = 8.5

# Hiển thị kiểu dữ liệu của từng biến
print("name:", name, "- Kiểu dữ liệu:", type(name))
print("age:", age, "- Kiểu dữ liệu:", type(age))
print("average_score:", average_score, "- Kiểu dữ liệu:", type(average_score))

